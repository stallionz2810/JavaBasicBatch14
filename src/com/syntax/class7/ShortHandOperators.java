package com.syntax.class7;

public class ShortHandOperators {

	public static void main(String[] args) {
		int i=10;
		
		i+=9;
		System.out.println(i);
		
		i-=5;
		System.out.println(i);
		i*=2;
		System.out.println(i);
		i-=10;
		System.out.println(i);
		i%=4;
		System.out.println(i);
				
		

	}

}
