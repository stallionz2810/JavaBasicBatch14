package com.syntax.class7;

public class IncrementandDecrement {

	public static void main(String[] args) {
		
		int num=90;
		num++;  //+1
		System.out.println(num);
		//to decrement a value of a variable by 1
		num--;
		System.out.println(num);
		
		int i=10;
		i--;
		i--;
		System.out.println(i);
		

	}

}
